package com.javaex.practice;

//아래 프로그램의 결과를 코드작성전에 먼저 예상해보고 코드를 작성해 확인해 보세요

public class Ex15 {
	
	public static void main (String[] args) {
		
		System.out.println( 12 / 5 - 3 );    			// -1
		System.out.println( 5 + 19 % 3 );				// 6
		System.out.println( 2 + 2 / 2 );				// 3
		System.out.println( 29 % 3 + 29 / 3 );			// 11
		System.out.println( 29 % 3 + 18.0 / 4 );		// 2 + 4.5 = 6.5
		System.out.println( (14 + 7/2) / 4 );			// 4
		System.out.println( (14 + 7/2.0) / 5 );			// 17 / 5 = 3.4
	}

}
